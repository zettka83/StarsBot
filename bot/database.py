async def get_random_question(category):
    async with aiosqlite.connect(DB_PATH) as db:
        async with db.execute("SELECT * FROM questions WHERE category = ? ORDER BY RANDOM() LIMIT 1", (category,)) as cursor:
            return await cursor.fetchone()

async def add_reward_log(telegram_id, stars):
    async with aiosqlite.connect(DB_PATH) as db:
        date = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
        await db.execute("INSERT INTO rewards (user_id, stars, date) VALUES (?, ?, ?)", (telegram_id, stars, date))
